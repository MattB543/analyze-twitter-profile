def determine_self_ids(args: argparse.Namespace, tweets_file: Path = None, likes_file: Path = None, bookmarks_file: Path = None) -> set:
    """Determine user's own Twitter IDs from various sources.
    
    Args:
        args: Parsed command line arguments
        tweets_file: Path to tweets file (optional)
        likes_file: Path to likes file (optional) 
        bookmarks_file: Path to bookmarks file (optional)
    
    Returns:
        Set of user IDs belonging to the data owner
    """
    self_ids = set()
    
    # 1. Check command line --self-id argument
    if args.self_id:
        self_ids.add(str(args.self_id))
        print(f"üÜî  Using self ID from command line: {args.self_id}")
        return self_ids
    
    # 2. Check command line --self-handle argument
    self_handle = args.self_handle
    
    # 3. Check environment variable MY_TWITTER_HANDLE if no CLI handle provided
    if not self_handle:
        self_handle = os.environ.get('MY_TWITTER_HANDLE')
        if self_handle:
            print(f"üÜî  Using self handle from environment variable: {self_handle}")
    
    # 4. Try to extract self IDs from tweets file first (original behavior)
    if tweets_file and tweets_file.exists():
        print("üîç  Detecting self IDs from tweets file...")
        try:
            lines = tweets_file.read_text(encoding="utf-8").splitlines()
            for line in lines:
                if not line.strip():
                    continue
                try:
                    obj = json.loads(line)
                    raw_data = obj.get("raw", {})
                    user_res = raw_data.get("core", {}).get("user_results", {}).get("result", {})
                    uid = user_res.get("rest_id") or user_res.get("id")
                    if uid:
                        self_ids.add(str(uid))
                except Exception:
                    continue
            if self_ids:
                print(f"‚úÖ  Found {len(self_ids)} self ID(s) from tweets file")
                if len(self_ids) > 1:
                    print(f"‚ö†Ô∏è  Multiple candidate self IDs detected: {sorted(self_ids)}. "
                          "Use --self-id to disambiguate.")
                return self_ids
        except Exception as e:
            print(f"‚ö†Ô∏è  Failed to read tweets file for self ID detection: {e}")
    
    # 5. If we have a self_handle but no tweets file, warn that we can't reliably determine ID
    if self_handle and not self_ids:
        print(f"‚ö†Ô∏è  Handle '{self_handle}' provided but no tweets file available for ID lookup.")
        print("üí°  Likes and bookmarks contain other people's tweets, so cannot reliably determine your ID from them.")
        print("üí°  Use --self-id to provide your Twitter user ID directly, or include a tweets_*.jsonl file.")
    
    if not self_ids:
        print("‚ö†Ô∏è  Could not determine self ID. Use --self-handle or --self-id for accurate self-identification.")
        print("üí°  Your tweets will not be labeled as '@me' in output files.")
        if not tweets_file:
            print("üí°  Tip: Self-identification works best when you have a tweets_*.jsonl file or provide --self-handle/--self-id")
    
    return self_ids








